# Generate a pre-zoomed .gtkw save file for GTKWave.
#----------------------------------------------------------------------------

"""
Generate a GTKWave .gtkw save file pre-zoomed to a specific time/cycle range
with given signals.

Usage:
  python3 vcd_gtkwave.py tb_arvern.vcd sig1 sig2 ... --cycles 540:620 --out debug.gtkw
  python3 vcd_gtkwave.py tb_arvern.vcd irq_software_i mstatus_mie trap_taken \\
      irq_suppress_post_mret --cycles 400:600 --out irq_debug.gtkw
  python3 vcd_gtkwave.py tb_arvern.vcd --from-file signals.txt --cycles 400:600 --out debug.gtkw
"""

import argparse
import os
import sys

sys.path.insert(0, os.path.dirname(__file__))
from vcd_trace import VCDParser, detect_clk_period


def read_signals_file(path):
    """Read signal names from a text file, one per line. Lines starting with # are comments."""
    names = []
    with open(path) as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith('#'):
                continue
            names.append(line)
    return names


def write_gtkw(out_path, vcd_path, sigs, t_start, t_end, vcd_display_path=None):
    """
    Write a GTKWave .gtkw save file.

    Parameters
    ----------
    out_path        : output .gtkw file path
    vcd_path        : absolute path to VCD file (used for dumpfile header)
    sigs            : list of (full_name, vid, width)
    t_start         : first tick of the view window
    t_end           : last tick of the view window (may be None)
    vcd_display_path: override path written into dumpfile directive
    """
    abs_vcd  = os.path.abspath(vcd_path)
    abs_gtkw = os.path.abspath(out_path)
    dumpfile_path = vcd_display_path if vcd_display_path else abs_vcd

    vcd_size = os.path.getsize(abs_vcd) if os.path.exists(abs_vcd) else 0

    # Zoom factor — fixed good default
    zoom = -18.0

    # Primary marker: midpoint of range, or t_start + 1000 if no end
    if t_end is not None:
        marker = (t_start + t_end) // 2
    else:
        marker = t_start + 1000

    # Build the *-zoom line: zoom factor, marker tick, then 26 x -1
    marker_suffix = ' '.join(['-1'] * 26)
    zoom_line = f'*{zoom:.1f} {marker} {marker_suffix}'

    # Derive the treeopen prefix from the VCD file stem (first component of first signal)
    treeopen = ''
    if sigs:
        first_full = sigs[0][0]
        parts = first_full.split('.')
        if len(parts) > 1:
            treeopen = parts[0] + '.'

    lines = []
    lines.append('[*]')
    lines.append('[*] Generated by vcd_gtkwave.py')
    lines.append('[*]')
    lines.append(f'[dumpfile] "{dumpfile_path}"')
    lines.append(f'[dumpfile_size] {vcd_size}')
    lines.append(f'[savefile] "{abs_gtkw}"')
    lines.append(f'[timestart] {t_start}')
    lines.append('[size] 1400 900')
    lines.append('[pos] -1 -1')
    lines.append(zoom_line)
    if treeopen:
        lines.append(f'[treeopen] {treeopen}')
    lines.append('[signals_width] 250')
    lines.append('[sst_expanded] 1')

    for full_name, _, width in sigs:
        if width > 1:
            lines.append('@22')
            # Append bit-range suffix
            lines.append(f'{full_name}[{width - 1}:0]')
        else:
            lines.append('@28')
            lines.append(full_name)

    # Trailing separator and pattern_trace markers
    lines.append('-')
    lines.append('[pattern_trace] 1')
    lines.append('[pattern_trace] 0')

    with open(out_path, 'w') as f:
        f.write('\n'.join(lines) + '\n')


def main():
    ap = argparse.ArgumentParser(
        description='Generate a GTKWave .gtkw save file for a VCD signal subset.',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    ap.add_argument('vcd',     help='Path to VCD file')
    ap.add_argument('signals', nargs='*', help='Signal name patterns')
    ap.add_argument('--cycles',    metavar='START:END',
                    help='Cycle range (e.g. 540:620)')
    ap.add_argument('--ticks',     metavar='START:END',
                    help='Raw tick range — overrides --cycles')
    ap.add_argument('--clk-period', type=int, metavar='N',
                    help='Clock period in ticks (auto-detected if omitted)')
    ap.add_argument('--out',       default='debug.gtkw', metavar='FILE',
                    help='Output .gtkw path (default: debug.gtkw)')
    ap.add_argument('--from-file', metavar='FILE',
                    help='Read signal names from a text file (one per line, # = comment)')
    ap.add_argument('--vcd-path',  metavar='PATH',
                    help='Override the dumpfile path written into the .gtkw')
    args = ap.parse_args()

    # Collect signal patterns
    patterns = list(args.signals)
    if args.from_file:
        patterns += read_signals_file(args.from_file)

    if not patterns:
        ap.error('Specify at least one signal pattern (positional or via --from-file)')

    vcd_parser = VCDParser(args.vcd)

    # Clock period
    clk_period = args.clk_period
    if clk_period is None:
        clk_period = detect_clk_period(vcd_parser)
        if clk_period:
            print(f'# Auto-detected clock period: {clk_period} ticks ({vcd_parser.timescale})',
                  file=sys.stderr)

    # Time range
    t_start, t_end = 0, None
    if args.ticks:
        parts = args.ticks.split(':')
        t_start = int(parts[0]) if parts[0] else 0
        t_end   = int(parts[1]) if len(parts) > 1 and parts[1] else None
    elif args.cycles:
        if not clk_period:
            ap.error('--cycles requires a clock period; use --clk-period or ensure hclk is in the VCD')
        parts = args.cycles.split(':')
        t_start = int(parts[0]) * clk_period if parts[0] else 0
        t_end   = int(parts[1]) * clk_period if len(parts) > 1 and parts[1] else None

    # Resolve signals
    sigs = vcd_parser.find_signals(patterns)
    if not sigs:
        print('No signals found.', file=sys.stderr)
        return 1

    write_gtkw(args.out, args.vcd, sigs, t_start, t_end,
               vcd_display_path=args.vcd_path)

    abs_out = os.path.abspath(args.out)
    vcd_name = os.path.basename(args.vcd)
    print(f'Generated: {abs_out}  (open with: gtkwave {vcd_name} {args.out})')
    return 0


if __name__ == '__main__':
    sys.exit(main())
